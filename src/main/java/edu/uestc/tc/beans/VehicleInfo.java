package edu.uestc.tc.beans;

import java.util.Date;

/**
 * 
 * @author MingHe 对应数据库中的车辆信息
 */
public class VehicleInfo {
	private Date Dtime;
	private int iLat;
	private int iLong;
	private int iNo;
	private int iSpeed;

	public VehicleInfo() {
	}

	public Date getDtime() {
		return Dtime;
	}

	public int getiLat() {
		return iLat;
	}

	public int getiLong() {
		return iLong;
	}

	public int getiNo() {
		return iNo;
	}

	public int getiSpeed() {
		return iSpeed;
	}

	public void setDtime(Date dtime) {
		Dtime = dtime;
	}

	public void setiLat(int iLat) {
		this.iLat = iLat;
	}

	public void setiLong(int iLong) {
		this.iLong = iLong;
	}

	public void setiNo(int iNo) {
		this.iNo = iNo;
	}

	public void setiSpeed(int iSpeed) {
		this.iSpeed = iSpeed;
	}

}
